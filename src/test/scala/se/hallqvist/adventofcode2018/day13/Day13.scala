  case class Cart(i: Int, p: Int, d: Int, t: Int)
  case class Stop(p: Int) extends Exception

  // Generate carts
  def gcs(s: String) = {
    val cs = mutable.Map[Int, Cart]()
    Iterator.from(0).scanLeft(0) { case (o, i) =>
      val p = s.indexWhere("<^>v".contains(_), o)
      if (p == -1) -1 else {
        val d = "<^>v".indexOf(s.charAt(p))
        cs(i) = Cart(i, p, d, 0)
        p + 1
      }
    }.dropWhile(_ != -1).next()
    cs
  }

  // Fixup map
  def f(s: String, l: Int, c: Cart) = {
    def w(s: String, i: Int) =
    // XXX does not deal with adjacent carts
      if (i < 0 || i >= s.length || !"-|/\\+".contains(s.charAt(i))) ' '
      else s.charAt(i)
    // XXX Just deals with found cases
    if (" |".contains(w(s, c.p - 1)) && " |".contains(w(s, c.p + 1)))
      s.substring(0, c.p) + "|" + s.substring(c.p + 1)
    else if (" -".contains(w(s, c.p - l)) && " -".contains(w(s, c.p + l)))
      s.substring(0, c.p) + "-" + s.substring(c.p + 1)
    else {
      println(s"failed to replace $c")
      s
    }
  }

  def part1(s: String) = {
    val l = s.indexOf("\n") + 1
    val cs = gcs(s)
    val ts = cs.foldLeft(s)((s, e) => f(s, l, e._2))

    def step(c: Cart) = {
      val np = c.p + Seq(-1, -l, 1, l)(c.d)
      if (cs.exists(_._2.p == np))
        throw new Stop(np)
      val n = ts.charAt(np)
      cs(c.i) = if ("-|".contains(n))
        Cart(c.i, np, c.d, c.t)
      else if ("\\/".contains(n))
        Cart(c.i, np, (7 - c.d - "/\\".indexOf(n) * 2) % 4, c.t)
      else if (n == '+')
        Cart(c.i, np, (c.d + c.t + 3) % 4, (c.t + 1) % 3)
      else
        throw new IllegalArgumentException(s"unknown track $n under $c")
    }

    try {
      Iterator.from(1).
        foreach { i => cs.values.toSeq.sortBy(_.p).toList.map(step) }
    }
    catch { case s: Stop => (s.p % l, s.p / l ) }
  }

  "part1" should "satisfy the examples given" in {
    assertResult((7, 3))(part1("""
/->-\        
|   |  /----\
| /-+--+-\  |
| | |  | v  |
\-+-/  \-+--/
  \------/   
""".stripPrefix("\n")))
  }

  "part1" should "succeed" in { info(part1(input).toString) }

  def part2(s: String) = {
    val l = s.indexOf("\n") + 1
    val cs = gcs(s)
    val ts = cs.foldLeft(s)((s, e) => f(s, l, e._2))

    def step(c: Cart) = if (cs contains c.i) {
      val np = c.p + Seq(-1, -l, 1, l)(c.d)
      if (cs.exists(_._2.p == np)) {
        cs -= cs.find(_._2.p == np).get._2.i
        cs -= c.i
      } else {
        val n = ts.charAt(np)
        cs(c.i) = if ("-|".contains(n))
          Cart(c.i, np, c.d, c.t)
        else if ("\\/".contains(n))
          Cart(c.i, np, (7 - c.d - "/\\".indexOf(n) * 2) % 4, c.t)
        else if (n == '+')
          Cart(c.i, np, (c.d + c.t + 3) % 4, (c.t + 1) % 3)
        else
          throw new IllegalArgumentException(s"unknown track $n under $c")
      }
    } else {}

    try {
      Iterator.from(1).
        foreach { i =>
          cs.values.toSeq.sortBy(_.p).toList.map(step)
          if (cs.size == 1) throw new Stop(cs.head._2.p)
        }
    }
    catch { case s: Stop => (s.p % l, s.p / l ) }
  }

  "part2" should "satisfy the examples given" in {
    assertResult((6, 4))(part2("""
/>-<\  
|   |  
| /<+-\
| | | v
\>+</ |
  |   ^
  \<->/
""".stripPrefix("\n")))
  }

  "part2" should "succeed" in { info(part2(input).toString) }
